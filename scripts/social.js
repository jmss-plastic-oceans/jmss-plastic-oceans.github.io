
function socialSetup() {

    document.getElementById("slack-img").onmouseover = function () { document.getElementById("slack-img").src = "images/social_icons/slack-color.svg"; };
    document.getElementById("slack-img").onmouseout = function () { document.getElementById("slack-img").src = "images/social_icons/slack-white.svg"; };
    
    document.getElementById("insta-img").onmouseover = function () { document.getElementById("insta-img").src = "images/social_icons/insta-color.webp"; };
    document.getElementById("insta-img").onmouseout = function () { document.getElementById("insta-img").src = "images/social_icons/insta-white.png"; };

    document.getElementById("fb-img").onmouseover = function () { document.getElementById("fb-img").src = "images/social_icons/facebook-color.png"; };
    document.getElementById("fb-img").onmouseout = function () { document.getElementById("fb-img").src = "images/social_icons/facebook-white.png"; };

}